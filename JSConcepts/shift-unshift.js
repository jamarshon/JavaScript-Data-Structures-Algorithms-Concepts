var fruits = ["Banana", "Orange", "Apple", "Mango"];
fruits.shift();

// Orange,Apple,Mango

// The shift() method removes the first item of an array, and returns that item.

var fruits = ["Banana", "Orange", "Apple", "Mango"];
fruits.unshift("Lemon","Pineapple");

// Lemon,Pineapple,Banana,Orange,Apple,Mango

